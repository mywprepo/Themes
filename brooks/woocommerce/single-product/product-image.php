global $post, $product;

?>
<div class="images" <?php count( $product->get_gallery_attachment_ids()) > 0 ? 'nopadding' : '' ?>  >
	<?php
		if ( has_post_thumbnail() ) {
			$attachment_count = count( $product->get_gallery_attachment_ids() );
			$props            = wc_get_product_attachment_props( get_post_thumbnail_id(), $post );
			$image            = get_the_post_thumbnail( $post->ID, apply_filters( 'single_product_large_thumbnail_size', 'shop_single' ), array(
				'title'	 => $props['title'],
				'alt'    => $props['alt'],
			) );

			if ($attachment_count == 0) echo apply_filters( 'woocommerce_single_product_image_html', sprintf( '%s', $image), $post->ID );
			if ($attachment_count >= 1) do_action( 'woocommerce_product_thumbnails' );
		} else {
			echo apply_filters( 'woocommerce_single_product_image_html', sprintf( '<img src="%s" alt="%s" />', wc_placeholder_img_src(), esc_html__( 'Placeholder', 'brooks' ) ), $post->ID );
		}


	?>
</div>
